using System.Collections.Generic;
using ProtoBuf;


/// <summary>
/// 物品数据
/// </summary>
[ProtoContract(ImplicitFields = ImplicitFields.AllFields)]
[ProtoInclude(7, typeof(EquipmentData))]
public class ItemData
{
    /// <summary>
    /// 物品等级
    /// </summary>
    public int itemLevel;
    /// <summary>
    /// 装备ID
    /// </summary>
    public int itemID;
    /// <summary>
    ///实例配置表ID
    /// </summary>
    public int instanceID;
    /// <summary>
    /// 模板ID
    /// </summary>
    public int templateID;
    /// <summary>
    /// 物品品质
    /// </summary>
    public int itemQuality;
    /// <summary>
    /// 物品数量
    /// </summary>
    public int sum;
    /// <summary>
    /// 物品类型
    /// </summary>
    public int itemType;
    /// <summary>
    /// 物品名字
    /// </summary>
    public string itemName;
    /// <summary>
    /// 物品图标名字
    /// </summary>
    public string itemIconName;

    public float resultItemLevel;

    public ItemData() { }
}

