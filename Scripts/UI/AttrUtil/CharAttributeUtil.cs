public partial class CharAttributeUtil
{
	public static float GetAttrValue(CharAttribute attr,string attrName)
	{
		switch(attrName)
		{
			case "finalHP":
			    return attr.finalHP;
			case "finalAP":
			    return attr.finalAP;
			case "finalSP":
			    return attr.finalSP;
			case "finalShield":
			    return attr.finalShield;
			case "finalArmor":
			    return attr.finalArmor;
			case "preAP":
			    return attr.preAP;
			case "finalAPBonus":
			    return attr.finalAPBonus;
			case "preSP":
			    return attr.preSP;
			case "finalSPBonus":
			    return attr.finalSPBonus;
			case "finalEnergyReg":
			    return attr.finalEnergyReg;
			case "preHP":
			    return attr.preHP;
			case "finalHPBonus":
			    return attr.finalHPBonus;
			case "preShield":
			    return attr.preShield;
			case "finalShieldBonus":
			    return attr.finalShieldBonus;
			case "finalShieldReg":
			    return attr.finalShieldReg;
			case "preArmor":
			    return attr.preArmor;
			case "finalArmorBonus":
			    return attr.finalArmorBonus;
			case "finalArmorReg":
			    return attr.finalArmorReg;
			default:
			  return 0;
		}
	}
}
